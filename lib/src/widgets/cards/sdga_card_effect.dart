part of 'sdga_card.dart';

/// The visual effect to be applied to the card.
///
/// This enum represents the different visual effects that can be applied to the
/// card component.
enum SDGACardEffect {
  /// Applies a shadow effect to the card, creating a
  /// subtle depth and elevation.
  shadow,

  /// Removes any shadow effect from the card,
  /// rendering it flat.
  noShadow,

  /// Applies a stroke or outline around the card,
  /// creating a border effect.
  stroke
}
